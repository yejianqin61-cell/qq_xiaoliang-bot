# ── 签文抽取 ─────────────────────────────────────────────────


def _hash_to_index(user_id: str, date_str: str, pool_size: int) -> int:
    """将 user_id + date 哈希映射为固定索引"""
    seed = f"{user_id}:{date_str}"
    hash_val = int(hashlib.sha256(seed.encode()).hexdigest(), 16)
    return hash_val % pool_size


def draw_fortune(user_id: str) -> str:
    """
    抽今日运势签（每人每天固定）

    Args:
        user_id: 用户唯一标识

    Returns:
        格式化好的运势卡片文本
    """
    today = datetime.now(CST).strftime("%Y-%m-%d")

    # 基于 user_id + date 设置随机种子，同天同人结果一致
    seed_val = _hash_to_index(user_id, today, 2**31)
    rng = random.Random(seed_val)

    # 加权随机抽取
    index = rng.choices(range(len(FORTUNE_POOL)), weights=WEIGHTS, k=1)[0]
    level, fortune, roast, do_good, do_bad = FORTUNE_POOL[index]

    card = (
        f"════ 今日运势 • {today} ════\n\n"
        f"📮 签运：【{level}】\n"
        f"📜 {fortune}\n\n"
        f"💀 小亮锐评：{roast}\n\n"
        f"✅ 宜：{do_good}\n"
        f"🚫 忌：{do_bad}\n"
    )

    return card


